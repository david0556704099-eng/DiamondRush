#include "Game.h"
#include <iostream>   

//  פיקסלים לכל תא 32 
static const int TILE_SIZE = 32;

Game::Game()
    : m_window(sf::VideoMode({ 1280, 720 }), "Diamond Rush"),
    m_physics(&m_map)  // רק ניגש למפה, לא מחזיק אותה
{
    m_window.setFramerateLimit(60); 

    // loadFromFile עלולה לזרוק runtime_error אם הקובץ לא נמצא
    // try-catch תופס את זה ומונע קריסה
    try {

        m_map.loadFromFile(std::string(ASSETS_DIR) + "/levels/level1.txt");
    }
    catch (const std::runtime_error& e) {
 
        std::cerr << "Error loading level: " << e.what() << std::endl;
        m_window.close(); 
    }
}

// ?????????????????????????????????????????????????????????????????????????????
// processEvents — מרוקן את תור האירועים של SFML פריים פריים
// ?????????????????????????????????????????????????????????????????????????????
void Game::processEvents()
{
    // הלולאה רצה עד שהתור מתרוקן
    while (const std::optional event = m_window.pollEvent())
    {
        // המשתמש לחץ על X — סגור חלון
        if (event->is<sf::Event::Closed>())
            m_window.close();

        if (const auto* keyPressed = event->getIf<sf::Event::KeyPressed>())
        {
            // ESC — יציאה מהמשחק
            if (keyPressed->code == sf::Keyboard::Key::Escape)
                m_window.close();
        }
    }
}

// ?????????????????????????????????????????????????????????????????????????????
// run — הלולאה הראשית. רצה עד שהחלון נסגר.
// סדר קבוע בכל פריים: קלט ? לוגיקה ? ציור
// ?????????????????????????????????????????????????????????????????????????????
void Game::run()
{
    sf::Clock clock; // שעון SFML — מתחיל לספור ברגע שנוצר

    while (m_window.isOpen())
    {
        // מאפס את השעון ומחזיר כמה זמן עבר
        float deltaTime = clock.restart().asSeconds();

        processEvents(); // קרא קלט
        update(deltaTime); // עדכן לוגיקה
        render();          // צייר
    }
}

// ?????????????????????????????????????????????????????????????????????????????
// update — מעדכן את כל הלוגיקה של המשחק
// ?????????????????????????????????????????????????????????????????????????????
void Game::update(float deltaTime)
{
    m_physics.update(deltaTime); // מפעיל פיזיקת אבנים — נפילה, בדיקת התנגשויות
}

// ?????????????????????????????????????????????????????????????????????????????
// render — מצייר את כל המפה פריים פריים
// סדר קבוע: clear ? draw הכל ? display
// ?????????????????????????????????????????????????????????????????????????????
void Game::render()
{
    // מוחק את הפריים הקודם — בלי זה כל פריים מצטבר על הקודם
    m_window.clear(sf::Color(20, 20, 30)); // כחול-אפור כהה — רקע המשחק

    // לולאה כפולה — עוברת על כל תא במפה (row-major: שורה אחר שורה)
    for (int r = 0; r < m_map.getRows(); ++r)
    {
        for (int c = 0; c < m_map.getCols(); ++c)
        {
            Tile& tile = m_map.getTile(r, c);

            sf::RectangleShape shape = tile.getShape();

            // ממקמים על המסך לפי מיקום בגריד:
            // תא (r=2, c=3) ? פיקסלים (x=96, y=64)
            shape.setPosition(sf::Vector2f(
                static_cast<float>(c * TILE_SIZE), // x = עמודה × 32
                static_cast<float>(r * TILE_SIZE)  // y = שורה × 32
            ));

            // מצייר על ה-buffer הנסתר — המשתמש עדיין לא רואה
            m_window.draw(shape);
        }
    }

    // מחליף את כל מה שציירנו מאחורי הקלעים, קופץ למסך בבת אחת
    m_window.display();
}